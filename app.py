# 2. ПОСТРОЕНИЕ ИНТЕРФЕЙСА GRADIO
# ==========================================
with gr.Blocks(title="Поиск видео по текстовому промпту", theme=gr.themes.Soft()) as demo:
    
    gr.Markdown("# 🎬 Поиск видео по текстовому описанию")
    gr.Markdown("Введите описание, и модель подберет подходящие видео из библиотеки.")
    
    with gr.Row():
        with gr.Column(scale=1):
            # Поле ввода промпта
            prompt_input = gr.Textbox(
                label="Текстовый промпт",
                placeholder="Например: 'Кот играет с мячом на зеленой траве'",
                lines=3
            )
            
            # Ползунок для выбора количества видео
            num_videos_input = gr.Slider(
                minimum=1, 
                maximum=10, 
                step=1, 
                value=3, 
                label="Количество видео для отображения"
            )
            
            # Кнопка запуска
            submit_btn = gr.Button("Найти видео", variant="primary")
            
        with gr.Column(scale=2):
            # Компонент для вывода видео. 
            # gr.Gallery автоматически поддерживает отображение видео, если пути ведут на .mp4 и т.д.
            video_output = gr.Gallery(
                label="Результат поиска",
                columns=2,
                rows=2,
                height="auto",
                object_fit="cover"
            )

    # Связываем кнопку с функцией обработки
    # Входы: [prompt_input, num_videos_input]
    # Выход: [video_output]
    submit_btn.click(
        fn=process_prompt_and_get_videos,
        inputs=[prompt_input, num_videos_input],
        outputs=[video_output]
    )

